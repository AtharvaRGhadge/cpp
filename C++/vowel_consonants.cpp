# include<iostream>
using namespace std;
//Check whether input character is vowel or consonant
int main(){
    char x;
    cout<<"enter a character";
    cin>>x;
    if (x=='a'|| x=='A'){
        cout<<x<<" is a vowel";
    }
    else if(x=='e'|| x=='E'){
        cout<<x<<" is a vowel";
    }
    else if(x=='i' || x=='I'){
        cout<<x<<" is a vowel";
    }
    else if(x=='o' || x=='O'){
        cout<<x<<" is a vowel";
    }
    else if(x=='u' || x=='U'){
        cout<<x<<" is a vowel";
    }
    else{
        cout<<x<<" is a consonant";
    }
}    


