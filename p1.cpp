# include<iostream>
using namespace std;
//Check whether input character is vowel or consonant
int main(){
    char x;
    cout<<"enter a character";
    cin>>x;
    if (x=='a'){
        cout<<x<<" is a vowel";
    }
    else if(x=='e'){
        cout<<x<<" is a vowel";
    }
    else if(x=='i'){
        cout<<x<<" is a vowel";
    }
    else if(x=='o'){
        cout<<x<<" is a vowel";
    }
    else if(x=='u'){
        cout<<x<<" is a vowel";
    }
    else{
        cout<<x<<" is a consonant";
    }
    return 0;
}    


